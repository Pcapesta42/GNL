#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*tab;
	int		lens1;
	int		lens2;

	if (!s1 || !s2)
		return (NULL);
	lens1 = ft_strlen(s1);
	lens2 = ft_strlen(s2);
	if (!(tab = ft_strnew(lens1 + lens2 + 1)))
		return (0);
	ft_strcpy(tab, s1);
	ft_strcat(tab, s2);
	return (tab);
}
